function generateFullCurriculum() {
  const entries: string[] = [];

  // –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –¥–Ω—è
  for (const theme of pythonThemes) {
    const template = theoryTemplates[theme.category] || theoryTemplates.basics;
    const theory = template(theme.topic, theme.day);
    const recap = generateRecap(theme.topic, theme.day);
    const tasks = generateTasks(theme.day, theme.topic, theme.category);

    const tasksJson = JSON.stringify(tasks, null, 6).replace(/"([^"]+)":/g, '$1:');

    entries.push(`  ${theme.day}: {
    day: ${theme.day},
    theory: \`${theory}\`,
    recap: '${recap}',
    tasks: ${tasksJson}
  }`);
  }

  const content = `import type { Difficulty } from '@/types';

export interface DayTask {
  id: string;
  difficulty: Difficulty;
  prompt: string;
  solutionHint?: string;
}

export interface DayContentData {
  day: number;
  theory: string;
  recap: string;
  recapTask?: DayTask;
  tasks: DayTask[];
}

// –ü–æ–ª–Ω—ã–π –Ω–∞–±–æ—Ä –∫–æ–Ω—Ç–µ–Ω—Ç–∞ –¥–ª—è –≤—Å–µ—Ö 90 –¥–Ω–µ–π (Python)
export const CURRICULUM_CONTENT: Record<number, DayContentData> = {
${entries.join(',\n')}
};

export const getDayContent = (day: number): DayContentData => {
  return CURRICULUM_CONTENT[day] ?? CURRICULUM_CONTENT[1];
};
`;

  // –°–æ—Ö—Ä–∞–Ω—è–µ–º —Ñ–∞–π–ª
  const outputPath = path.join(__dirname, '../src/data/curriculum-content.ts');
  fs.writeFileSync(outputPath, content, 'utf-8');

  console.log(`‚úÖ –°–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω –∫–æ–Ω—Ç–µ–Ω—Ç –¥–ª—è ${pythonThemes.length} –¥–Ω–µ–π`);
  console.log(`üìÅ –§–∞–π–ª —Å–æ—Ö—Ä–∞–Ω—ë–Ω: ${outputPath}`);
}

// –ó–∞–ø—É—Å–∫
generateFullCurriculum();
